#SWAPPING NUMBERS

num1 = input("Enter the first number: ")
num2 = input("Enter the second number: ")

print("Value num 1 is: ", num1)
print("Value num 2 is: ", num2)

#swaping numbers

num1, num2 = num2, num1

print("Value num 1 after swapping is: ", num1)
print("Value num 2 after swapping is: ", num2)
